echo "PHOTON_KAFKA_TOPIC_PREFIX=${PHOTON_KAFKA_TOPIC_PREFIX}"
echo "PHOTON_KAFKA_TOPIC_SHARDS=${PHOTON_KAFKA_TOPIC_SHARDS}"
