export PHOTON_KAFKA_REPLICAS="${PHOTON_KAFKA_REPLICAS:-1}"
export PHOTON_KAFKA_REPLAY_CURSOR="${PHOTON_KAFKA_REPLAY_CURSOR:-stream_seq}"
export PHOTON_KAFKA_SYNC_ACK="${PHOTON_KAFKA_SYNC_ACK:-1}"
