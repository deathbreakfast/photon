#!/usr/bin/env bash
# Export PHOTON_KAFKA_* for local single-node Kafka lab.
set -euo pipefail

export PHOTON_KAFKA_BROKERS="${PHOTON_KAFKA_BROKERS:-127.0.0.1:9092}"
